def name(sid):
    return lookup[sid][1]

def rideTheTrain(start='101'):
    print("Starting at {0}".format(name(start)))
    current = start
    while True:
        print("At {0}. Next stops are:".format(name(current)))
        for i in range(len(lookup[current][0])):
            print('  {0} - {1}  ({2})'.format(i,name(lookup[current][0][i]),lookup[current][0][i][0]))        
        selection = raw_input('  Select Stop: ')
        try:
            current = lookup[current][0][int(selection)]
        except ValueError:
            pass
        except IndexError:
            pass

